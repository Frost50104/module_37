// Задание 6.4 - Рисование поля для крестиков-ноликов

// Создаём двумерный массив 3x3
const field = [
    ['x', 'o', 'x'],
    ['o', 'x', 'o'],
    ['x', 'o', 'x']
];

// Выводим поле в консоль
for (let i = 0; i < field.length; i++) {
    console.log(field[i].join(' '));
}

// Вывод:
// x o x
// o x o
// x o x
